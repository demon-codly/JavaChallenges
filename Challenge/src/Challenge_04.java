/*
4.Show the following patterns using single print statement:
1. Right Half Pyramid
2. Reverse Right Half Pyramid
3. Left Half Pyramid
 */

public class Challenge_04 {
    public static void main(String[] args) {
        System.out.println("Printing the Pattern\n");
        System.out.println("Right Half Pyramid\n");
        System.out.println("* * * * *\n" +
                "* * * *\n" +
                "* * *\n" +
                "* *\n" +
                "*\n" );

        System.out.println("Reverse Right Half Pyramid\n");
        System.out.println("*\n" +
                "* *\n" +
                "* * *\n" +
                "* * * *\n" +
                "* * * * *\n");

        System.out.println("Left Half Pyramid\n");
        System.out.println("        *\n" +
                "      * *\n" +
                "    * * *\n" +
                "  * * * *\n" +
                "* * * * *");
    }
}
