//3.Show the following patterns:
// 1. Right Half Pyramid
// 2. Reverse Right Half Pyramid
// 3. Left Half Pyramid
public class Challenge_03 {
    public static void main(String[] args) {
        System.out.println("Printing the Pattern\n");
        System.out.println("Right Half Pyramid\n");
        System.out.println("* * * * *");
        System.out.println("* * * *");
        System.out.println("* * *");
        System.out.println("* *");
        System.out.println("*\n");


        System.out.println("Reverse Right Half Pyramid\n");
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * *\n");

        System.out.println("Left Half Pyramid\n");
        System.out.println("        *");
        System.out.println("      * *");
        System.out.println("    * * *");
        System.out.println("  * * * *");
        System.out.println("* * * * *");
    }
}
